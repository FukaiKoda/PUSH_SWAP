#include "ft_printf.h"

int	putlstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (i += putlstr("(null)"));
	while (s[i])
	{
		write (1, &s[i], 1);
		i++;
	}
	return (i);
}
